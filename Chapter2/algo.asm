.MODEL SMALL
.STACK 100H

.DATA
    X DW 11                    ; 输入变量X
    Y DW 2222                    ; 输入变量Y
    Z DW 2221                   ; 输入变量Z
    V DW 2                    ; 输入变量V
    RESULT DW ?               ; 计算结果

.CODE
.STARTUP
    MOV AL,80H
    NEG AL
    MOV AX, X
    IMUL Y
    MOV CX, AX
    MOV BX, DX
    MOV AX, Z
    CWD
    ADD CX, AX
    ADC BX, DX
    SUB CX, 540
    SBB BX, 0
    MOV AX, V
    CWD
    SUB AX, CX
    SBB DX, BX
    IDIV X
    MOV RESULT, AX

.EXIT 0

END