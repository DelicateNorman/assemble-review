;把HEX打印成16进制数
.MODEL SMALL
.STACK 100H
.DATA
HEX DB 00B9H
.CODE
.STARTUP
MOV AL,HEX
MOV CL,4
SHR AL,CL
CALL HEXDISPLAY
MOV AL,HEX
CALL HEXDISPLAY
.EXIT 0

HEXDISPLAY PROC
PUSH AX
PUSH BX
AND AL,0FH
ADD AL,30H
MOV BL,39H
CMP AL,BL
JBE DONE
ADD AL,7H
DONE:
MOV DL,AL
MOV AH,2
INT 21H
POP BX
POP AX
RET
HEXDISPLAY ENDP
END